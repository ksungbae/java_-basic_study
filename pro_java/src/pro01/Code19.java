package pro01;

public class Code19 {

	public static void main(String[] args) {
		int a=10;
		int b=0b1010;
		int c=012;
		int d=0xA;
		System.out.println("a: "+a);     
		System.out.println("b: "+b);        //2진수: 숫자 앞에 0b, 0B
		System.out.println("c: "+c);        //8진수: 숫자 앞에 0
		System.out.println("d: "+d);        //16진수: 슷지 잎에 0x, 0X

	}

}
