package pro01;

public class Code13 {

	public static void main(String[] args) {
		byte b1=126;
		System.out.println(b1);
		b1++;
		System.out.println(b1);
		b1++;
		System.out.println(b1);
		
		byte b2=-127;
		System.out.println(b2);
		b2--;
		System.out.println(b2);
		b2--;
		System.out.println(b2);  //byte의 범위는 -127부터 128까지

	}

}
