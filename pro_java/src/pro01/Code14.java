package pro01;

public class Code14 {

	public static void main(String[] args) {
		short s1=32767;
		System.out.println(s1);
		s1++;
		System.out.println(s1);
		s1++;
		System.out.println(s1);
		
		short s2=-32767;
		System.out.println(s2);
		s2--;
		System.out.println(s2);
		s2--;
		System.out.println(s2);  //short 범위는 -32768에서 32767까지이다
		
		

	}

}
